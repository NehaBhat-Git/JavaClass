// Class definition: ImplicitTypeCasting
class ImplicitTypeCasting {
    // Main method: entry point of the program
    public static void main(String[] args) {
        // Declare a byte variable 'a' and initialize it with the value 10
        byte a = 10;
        // Implicitly cast 'a' from byte to int and store it in 'b'
        int b = a;
        // Print the values of 'a' and 'b' to the console
        System.out.println("a : " + a);
        System.out.println("b : " + b);
    }
}
