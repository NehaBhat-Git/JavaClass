// Define a class named HexaDecimal
class HexaDecimal {
    // Main method - serves as the entry point for the program
    public static void main(String [] args) {
        // Define an integer variable 'HexD' and assign it a hexadecimal value '0xA'
        // '0x' is the prefix used in Java to indicate a hexadecimal value
        // 'A' in hexadecimal is equivalent to 10 in decimal
        int HexD = 0xA;
        
        // Print the decimal equivalent of the hexadecimal number
        // Since 'A' in hexadecimal is 10 in decimal, it prints "Hexa number into Decimal Conversion 10"
        System.out.println("Hexa number into Decimal Conversion " + HexD);
    }
}
